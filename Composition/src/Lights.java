public class Lights {

    private int number;
    private String type;

    public Lights(int number, String type) {
        this.number = number;
        this.type = type;
    }

    public int getOnLights()
    {
        return number;
    }

    private int getNumber() {
        return number;
    }

    private String getType() {
        return type;
    }
}
